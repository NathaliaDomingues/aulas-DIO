public class keyPrimitivo {
    public static void main(String[] args) {
        //tipos primitivos
        byte numero = 1;                   // para especificar o tamanho, quantidade da memória ou da capacidade de armazenamento
        short numero2 = 2;                 // armazena valores inteiros menores que podem ser armazenados no INT
        long numero3 = 3L;                 // usado para representar números inteiros maiores do que o INT pode suportar
        float numero4 = 4.0f;              // representa números de pónto flutuante de precisão simples
        var numero5 = 5.0d;                // variável do tipo DOUBLE
        char caractere = 'a';               // usado para representar um único caractere
        boolean bool = false;              // usado para avaliar condições e tomar decisões no códigos
    }
}